package com.example.demo;

public class Calculator {
    public int sum(int x, int y) {
        return x + y;
    }

    public int subtract(int x, int y) {
        return x - y;
    }

    public int division(int x, int y) {
        return x/y;
    }

    public int multiply(int x, int y) {
        return x * y;
    }
}
